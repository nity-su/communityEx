export default function CallEther() {
  const ethereum = window.ethereum;

  function connect() {
    ethereum
      .request({
        method: "wallet_switchEthereumChain",
        params: [{ chainId: "0xD2AF" }],
      })
      .then(console.log)
      .catch((error) => {
        if (error.code === 4001) {
          // EIP-1193 userRejectedRequest error
          console.log("Please connect to MetaMask.");
        } else {
          console.error(error);
        }
      });
  }
  async function transaction() {
    const web3 = new Web3(ethereum);
    const contract = new web3.eth.Contract(
      abi,
      "0xEb9B61B145D6489Be575D3603F4a704810e143dF"
    );
    await contract.methods
      .name()
      .call({ from: "0x145B667e023c016FE07dDB01a8B211C5b45ef2eC" })
      .then(console.log);
  }

  return (
    <>
      <div onClick={connect} style={{ fontSize: "72px" }}>
        callEther
      </div>
      <div onClick={transaction} style={{ fontSize: "72px" }}>
        거래!!!!!
      </div>
    </>
  );
}

